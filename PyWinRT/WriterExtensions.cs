    public static void WriteNamespaceModuleInitFunction(
        this IndentedTextWriter w,
        QualifiedNamespace ns,
        IReadOnlyDictionary<string, Package> packageMap,
        Members members,
        int dependencyDepth,
        string moduleSuffix
    )
    {
        w.WriteLine($"PyMODINIT_FUNC PyInit_{ns.NsModuleName}{moduleSuffix}(void) noexcept");
        w.WriteBlock(() =>
        {
            w.WriteLine($"using namespace py::cpp::{ns.Namespace.ToCppNamespace()};");
            w.WriteBlankLine();

            w.WriteLine("if (py::import_winrt_runtime() == -1)");
            w.WriteBlock(() => w.WriteLine("return nullptr;"));
            w.WriteBlankLine();

            w.WriteLine("py::pyobj_handle module{PyModule_Create(&module_def)};");
            w.WriteBlankLine();

            w.WriteLine("if (!module)");
            w.WriteBlock(() => w.WriteLine("return nullptr;"));
            w.WriteBlankLine();

            if (members.Classes.Count != 0 || members.Interfaces.Count != 0)
            {
                w.WriteLine("auto inspectable_meta_type = py::get_inspectable_meta_type();");
                w.WriteLine("if (!inspectable_meta_type)");
                w.WriteBlock(() => w.WriteLine("return nullptr;"));
                w.WriteBlankLine();
            }

            w.WriteLine("auto object_type = py::get_object_type();");
            w.WriteLine("if (!object_type)");
            w.WriteBlock(() => w.WriteLine("return nullptr;"));
            w.WriteBlankLine();

            w.WriteLine("py::pyobj_handle object_bases{PyTuple_Pack(1, object_type)};");
            w.WriteBlankLine();
            w.WriteLine("if (!object_bases)");
            w.WriteBlock(() => w.WriteLine("return nullptr;"));
            w.WriteBlankLine();

            foreach (
                var bts in members
                    .Classes.Where(c => c.CircularDependencyDepth == dependencyDepth)
                    .Select(c =>
                        (
                            c.Type.BaseType,
                            DependencyDepth: c.Type.BaseType.GetCircularDependencyDepth()
                        )
                    )
                    .Where(t =>
                        t.BaseType.Namespace != "System"
                        && (
                            t.BaseType.GetQualifiedNamespace(packageMap) != ns
                            || t.DependencyDepth != dependencyDepth
                        )
                    )
                    .GroupBy(t => (t.BaseType.GetQualifiedNamespace(packageMap), t.DependencyDepth))
            )
            {
                var (bns, bdd) = bts.Key;
                var suffix = bdd == 0 ? "" : $"_{bdd + 1}";

                w.WriteLine(
                    $"py::pyobj_handle {bns.PyModuleAlias}{suffix}_module{{PyImport_ImportModule(\"{bns.PyPackageModule}.{bns.NsModuleName}{suffix}\")}};"
                );
                w.WriteLine($"if (!{bns.PyModuleAlias}{suffix}_module)");
                w.WriteBlock(() => w.WriteLine("return nullptr;"));
                w.WriteBlankLine();

                foreach (
                    var bt in bts.Select(t => t.BaseType)
                        .Distinct(new TypeReferenceEqualityComparer())
                )
                {
